var countSheep = function (num){
  // Setup a string to tabulate the total number of sheep
  let newString = "";
  
  // for loop that iterates through adding each instance to tabulation string
  for (let i = 1; i <= num; i++) {
    newString += `${i} sheep...`;
  }
  // return the finished string
  return newString;
}


